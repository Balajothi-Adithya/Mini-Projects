class Employee{
    String name;
    String no;
     Employee(String name,String no){
        this.name=name;
        this.no=no;
    }
    String getName()
    {
        return name;
    }
    void setName(String name)
    {
        this.name=name;
    }
    String getNo()
    {
        return no;
    }
    void setNo(String no)
    {
        this.no=no;
    }
}